 	package logica;

public class Danyo {

	private String idDanyo; //CP
	private String zona;
	private String descripcion;
	
	
	public String getIdDanyo() {
		return idDanyo;
	}
	public void setIdDanyo(String idDanyo) {
		this.idDanyo = idDanyo;
	}
	public String getZona() {
		return zona;
	}
	public void setZona(String zona) {
		this.zona = zona;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
}
